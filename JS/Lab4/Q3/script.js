var para = document.getElementById("PAR");
function ChangeFont(x){
    para.style.fontFamily = x;

}
function ChangeAlign(y){
    para.style.textAlign = y;
}
function ChangeHeight(height){
    para.style.lineHeight = height;
}
function ChangeLSpace(space){
    para.style.letterSpacing = space;
}
function ChangeIndent(indent){
    para.style.textIndent = indent;
}
function ChangeTransform(transformType){
    para.style.textTransform = transformType;
}
function ChangeDecorate(decoration){
    para.style.textDecoration = decoration;
}
function ChangeBorder(borderType){
    para.style.borderStyle = borderType;
}
function ChangeBorderColor(color){
    para.style.borderColor = color;
}
